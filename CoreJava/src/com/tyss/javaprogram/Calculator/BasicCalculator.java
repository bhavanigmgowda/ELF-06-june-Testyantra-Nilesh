package com.tyss.javaprogram.Calculator;

public class BasicCalculator {
	
   
	/* method for add two number    */
	double add(int a,int b) {  
		return a + b;
		 
	}
	/* method for Subtract  two number    */
	double subtraction(int a,int b) {
		int c = a - b;
		return c;
	}
	/* method for Multiplication  of two number    */
	double multiplication(int a,int b) {
		int c = a * b;
		return c;
	}
	/* method for Multiplication  of two number    */
	double Division(int a,int b) {
		int c = a / b;
		return c;
	}
}
